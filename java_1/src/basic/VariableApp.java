package basic;

public class VariableApp {
	public static void main(String[] args) {
		int su;
		
		su=100;
		System.out.print("초기값 = ");
		System.out.println(su);
		
		su=200;
		System.out.print("변경값 = ");
		System.out.println(su);	
		System.out.println("===========");
		int num=100;
		System.out.println("num = "+num);
		;
		
	}
}